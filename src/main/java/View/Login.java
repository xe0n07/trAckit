    private void show_passwordActionPerformed(java.awt.event.ActionEvent evt) {                                              
        if (show_password.isSelected()) {
            password.setEchoChar((char)0); // Show password
        } else {
            password.setEchoChar('â€¢'); // Hide password (you can also use '*')
        }
    }                                             

    private void LoginBtnActionPerformed(java.awt.event.ActionEvent evt) {
    String uname = username.getText().trim();
    String pass = new String(password.getPassword());
    if (Controller.Controller.loginUser(uname, pass)) {
        // Remember Me logic
        Preferences prefs = Preferences.userRoot().node(getClass().getName());
        if (jCheckBox1.isSelected()) {
            prefs.put("remembered_username", uname);
        } else {
            prefs.remove("remembered_username");
        }

        javax.swing.JOptionPane.showMessageDialog(
            this,
            "Welcome to Trackit Inventory Management Software!",
            "Welcome",
            javax.swing.JOptionPane.INFORMATION_MESSAGE
        );
        new Main_Landing().setVisible(true);
        this.dispose();
        }
    }                                       

